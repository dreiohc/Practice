import UIKit

class YellowShade {
	
	var cream: UIColor{
		get{
			return UIColor(hexString:"#FFFFCC")
		}
	}
	var lemon: UIColor{
		get{
			return UIColor(hexString:"#FDFF00")
		}
	}
	var gold: UIColor{
		get{
			return UIColor(hexString: "#FFD700")
		}
	}
	
	var khaki: UIColor{
		get{
			return UIColor(hexString: "#C3B091")
		}
	}
	
}
